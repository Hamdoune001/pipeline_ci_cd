"""Module simple_math contenant des opérations mathématiques simples."""

class SimpleMath:
    """Classe avec des méthodes mathématiques simples."""

    @staticmethod
    def addition(a, b):
        """Additionne deux nombres."""
        return a + b

    @staticmethod
    def soustraction(a, b):
        """Soustrait b de a."""
        return a - b

